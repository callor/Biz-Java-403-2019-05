package com.biz.myclass;

public class Oop_08 {

	public static void main(String[] args) {
		// int형에는 없는 다양한 값, 기능들을
		// Integer wrapper 클래스는 포함하고 있다.
		// 그중 int형의 가장 작은값과 가장 큰값을 알고자할때
		// 사용하는 값이다.
		System.out.println("정수는 : " + Integer.MIN_VALUE
						+ " ~ " + Integer.MAX_VALUE 
						+ "까지이다");

		System.out.println("실수는 : " + Float.MIN_VALUE
				+ " ~ " + Float.MAX_VALUE 
				+ "까지이다");
		
		
	}

}
