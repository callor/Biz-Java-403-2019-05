package com.biz.exec;

public class Exec_05 {

	public static void main(String[] args) {
		// 숫자 30과 숫자 40을 각각 변수에 저장하고
		// 덧셈결과를 다른 변수에 저장한 후
		// 결과를 콘솔에 보이시오
		
		System.out.println(30+40);
		
		
		int num1 = 30; // 변수 첫글자 항상 영문소문자
		int num2 = 40; // 변수는 의미있는 단어
		
		int sum = num1 + num2; // 변수 이름만 보고도 어떤 일을하는지 짐작할수 있도록
		
		System.out.println(sum);
		

	}

}
